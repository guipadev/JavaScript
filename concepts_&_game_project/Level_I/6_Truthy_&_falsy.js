/*
VALORES TRUTHY Y FALSY
Hacen referencia a que tiene un valor  boolean por defecto ya sea true o false

Para determinar sus valores podemos utilizar la funcion Boolean(dato)
*/

Boolean(5)	//true
//Cualquier numero sera true

Boolean(0)	//false

Boolean("")	//false		vacio

Boolean(" ") //true		espacio vacio

Boolean(function(){})	//true

Boolean(null)	//false

Boolean(undefined)	//false

Boolean(NaN)	//false

Boolean(false)	//false

Boolean(true)	//true

// Algunas letras son true y otras false

