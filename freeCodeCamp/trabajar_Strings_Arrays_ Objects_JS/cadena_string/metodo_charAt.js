/**
 * El método .charAt()
 * Muchas veces, cuando trabajamos con cadenas, queremos acceder a un carácter en un cierto
 * índice de la cadena.
 * Puede hacer esto con el método charAt() o con la indexación, de la misma manera que
 * tratamos una matriz.
 */

// Let's say we want to access the first character of a given string
let string = "Hello World";

// using indexing
let first1 = string[0]; // output 'H' and remember that indexing starts at 0

// using the charAt() method
let first2 = string.charAt(0); // output 'H'
