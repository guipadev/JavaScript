/**
 * El método .concat()
 * A menudo es necesario unir cadenas de texto en un programa para crear una nueva cadena
 * de texto. Esto se llama concatenación.
 *
 * Ahora, para la concatenación de cadenas, podemos usar el método concat().
 * Se parece a lo siguiente.
 * Una nota importante es que este método devuelve una nueva cadena sin mutar la original.
 */

let string = "Hello";

// String concatenation using the concat method

let string1 = string.concat(" World"); // output 'Hello World'
